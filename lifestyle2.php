                    <article><p><h2>My story:</h2>
                      "We are 5 sisters. When my youngest sister was 3 months old, my father left for India. Since then, my mother raised us because he never returned. After that, father never called. Father never sent money home. Mother was left alone with 5 girls.
                      Mother could have run away with another man if she had wanted, but unlike father, she chose not to abandon us.

                        </p>
                    <p>
                      She was a healthy woman and she took good care of us. When we grew up to be young women, four of us married and went to our new homes. Mother stayed with our youngest sister. We visited her on occasions, but when she got sick we started visiting her more frequently. Sometimes she would talk about our father. She missed him. Some things are unsaid, you know. I think sometimes she blamed herself. She thought Father left because she could not bear him a son. Sons are thought to be more important than daughters in our society.
                      As days passed, mother started eating less and her body became frail.
                      We took her to the hospital and after months of tests, the doctors confirmed it was cancer.
                      A big tragedy had befallen us.<br>
                      We had no financial support. We had no father to go to. At the time, my sister was working abroad so she was able to contribute financially for mother's treatment. She was working in Kuwait as a babysitter. We started taking my mother to a hospital in the Chitwan district for her chemotherapy. Progress was very slow. In desperation, she would talk about Father and tell us that her wish was to meet him one last time and ask him why he left."<br>

                      “I think God heard her plea. It was during that time one of my sisters found my father on Facebook. We contacted him. We told him about Mother’s illness and begged him to return for mother, if not for us. We told her about Mother’s weakening body and mind. But he showed no desire to return.
                      Mother did not take it well. Her health deteriorated further. She could not swallow food properly.
                      Then more tragedy hit us.
                      The lockdown due to COVID-19 had started and we were not able to take her to the hospital for chemotherapy. Everything was closed and it was difficult for us to transport her to the hospital every few weeks. We all watched and cried. Sometimes together, sometimes alone.
                      <br>
                      One day I called my father in desperation. I yelled at him.
                      I told him that Mother was dying and if he has some heart left in him, he should come. I told him we will not have any bitter words for him. After crying and pleading, he agreed to come. When the lockdown eased, he came. But it was too late. Mother was slowly dying.
                      <br>
                      Mother died not long after father came. Father stayed for 45 days, did mother’s last rites and once again, left without telling us. I do not know what was in his heart. We called him again and asked him to return but he would not listen. So we let him go. But I take solace in feeling that Mother died in peace because she was able to see the man she had once loved.” <br>
                      "That was my personal story of hurt. But even I have my problems. I am an active member of the community. I work as a community health worker. When the coronavirus pandemic first started, I did not know much about it. But it was my duty to educate myself. So I would watch TV, the internet and listen to the radio to understand more. I would tell people about the virus. I started explaining the public announcements to the villagers. Most of the villagers would follow my advice. It was maybe because being a health worker in the community, I had also become their confidante in times of trouble. And because they had seen me go through so much family turmoil, we shared a belief that life is difficult. Because of my own pain, I could understand the pain of others and give them hope. People were stressed. Children talked about the virus and wanted to go back to school. Those who had some disposable money could afford the disposable masks. They would wash the disposable mask in Dettol liquid, dry it and wear it again until it tore. Some could not even afford to wear these masks and I understood that asking them to buy masks when they have no money would be insensitive. It was only when some organisations started distributing masks and soap that everyone got to wear masks.
                      Today those who returned home during and after the lockdown are slowly leaving to find work again abroad. Some to Qatar, some to Malaysia and some to India. There are many children who will grow up without truly knowing their mother or father. "

