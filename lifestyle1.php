                    <article><p><h2>Part 1</h2>
                      “My grandmother was a visionary woman.
                      She would tell me about the importance of water. I wonder who gave her the awareness. She did not go to school. She did not read about the conservation of nature. However, she knew. She knew what living with nature meant. Trees, rivers, hills and mountains were revered during those times. Development never meant cutting down forests and polluting the water.
                      Somehow, her words became ingrained in my mind.
                        </p>
                    <p>
                        As I grew up new roads started to be built in and around these hills.
                        More people came, markets started to become denser and the population increased.
                         There would be 100 trees and one man. Now, there are 100 men and one tree.
                         They deforested if you see. All of the trees, all of the shrubs and all of the jungle.
                         The whole Chure region became deserted. Water became scarce. The water source dried up.
                        I witness this and it hurt me. It would take me back to my grandmother who spoke of the importance of water. <br>
                        I needed to do something about it. I knew that I could not do this on my own. How can one man preserve a natural water system? All the members of the community needed to come together.
                        I joined a conservation group formed by Rural Development Center (RDC) supported by Paani Program that was involved in mapping out and searching water sources and studying why they dried up.
                        This has something I had always wanted to do. Every day, I would go out with a few of my colleagues and search for water sources.
                        In those years, I realized that the rampant deforestation around the water source was the primary reason for the depletion of water.
                        In addition, the number of people and settlements had increased which was not proportionate with the natural resources available in the area. I remember, in Dolakot, buffaloes would bathe in those ponds, which were naturally recharging rainwater to the ground for ages. Due to such practices, we had enough water in our springs but now it was all gone. When I witness these water sources drying up one after the other, it scared me. I knew that if it continued one day we would all die of thirst...<br>
                        <h2>Part 2</h2>
                        …With the support from Paani Project, we started to work to restore water sources. There were several discussions, training, surveys and ideas both from experts and communities leaders. We started planting trees and shrubs around the water sources. We gathered communities and shared our understanding of how deforestation and open grazing around the water sources contributed to the depletion of water. We built water eyebrows trench and recharge ponds and pits where rainwater would accumulate and help keep the ground moist. We fenced areas to block rain debris from flowing down the hill and blocking the water source.<br>
                        Eventually, we started seeing changes in the water source. When the villagers started seeing how the spring sources restored, they finally conclude that what we had advocated was true and that preserving was possible. It could be done in a natural and organic way. This recharged everyone’s perception of conservation. Some even contributed their land to the protected area around the water source.
                        <br>
                        Today, we have enough drinking water and enough water for the animals. And it was only possible through community agreement and collective effort. For me, I thank my grandmother for instilling the values in me through which I could mobilize my family and neighbours to support my passion. Moreover, thanks to the Paani project for delivering technical knowledge for protecting our valuable spring sheds that emphasized continuation of our own indigenous practice. If she were alive today, I would take her to these water sources and show her with pride what we have achieved." <br>
                        (Pratiman Gharti Magar, Alitaal 8, <br>Kurjeni, Dadeldhura)<br>
