                    <article><p>
                      For ten days we walked through the Himalaya mountains, along rivers, small villages and with a view of the most beautiful peaks and glaciers.<br> We walked a large part of the Annapurna Circuit, a beautiful track through the Himalayas of Nepal. In the end, we even crossed one of the highest mountain passes in the world, the Thorong La Pass, 5416 meters high!hat is it they say about time flying while you’re having fun?!) — and even more so that we’ve never actually shared the full story of what went down here on our blog!
