                    <h1>LADY ON ARTISTIC JOURNEY: BIDHATA KC</h1>
                    <img src="images/bidhata.jpg" alt="post img" class="pull-left img-responsive postImg img-thumbnail margin10">
                    <article><p>
                      Bidhata KC first visited Mustang in 2014, and was completely enchanted.
                      While she’s had a difficult time forgetting her trek in the shadow of snow-capped mountains, it’s Mustang’s whitewashed homes, with splashes of red and orange, that stuck with KC the most.
                      “I felt as if the walls of the homes had their own spiritual auras,” said KC.

                        </p>
                    <p>
                        Se had an interest in art since he was a child, but he never thought of it as a career.
                        Her Interest reignited when one of his friend showed him a picture posted in a national daily.
                        After seeing that photo, she really wanted to draw it. This was in 2010 between the periods of June – July. And then his love for art was put on hold when he pursued the career of an accountant.<br>
                        KC has since returned to the region several times, venturing into new areas around Mustang—from Lo Manthang to Kagbeni.
                        As an artist, painting became a form of remembering her trips and it was only natural that she would one day exhibit the art that was piling up.<br>
                        To bring Mustang to Kathmandu, KC has since put up four exhibitions on Mustang’s structures
                        (including a prequel of the same series last year). Currently, KC’s Structural Beauty, her series on Mustang, is on display on the narrow space of the Dalai-la Boutique Hotel.
                        Six canvases are put up, shaped in circles, rectangles and squares.
                        In one, against a clear blue sky, is an abstraction of a house from Mustang.
                        However, due to a lack of dimension and shadow, it appears as a two-dimensional broken wall fixed upon a circular canvas.
                        The same image is circulated on the invitations and posters of the exhibition; it’s clean, clear and pleasing to the eye. <br>
                        The five other acrylic paintings are similar. From afar, the images are captivating: a broken house placed at the bottom of the frame or a soft orange on roofs that subtly complements the blue background.
                        They exude calm.<br>
                        According to KC, her art is supposed to be a little vague. “I went for abstract over a familiar experience,” said KC. “Rather than a direct imagery of the place, I want them to be indirect and based upon my art style.”
                        KC’s colour palette for this particular series explores flat neutral tones, along with a few of the five colours of Buddhism. <br>

                        According to KC, holy colours extracted from nature are methodically dripped onto houses to protect families from evil spirits.
                        KC’s dominant colour is blue, associated with purity and healing; white with learning and knowledge; red with life force and preservation; and yellow with rootedness and renunciation. Green, associated with balance and harmony, isn’t found in Mustang.

                       “I have not changed the colours,” said KC. “They have been presented the way they are being used.”

                        In Mustang, KC says, even new houses appear fragile, accumulating cracks right after they have been constructed. Many houses are broken and provide a different visual aesthetic for viewers. While KC adores the old houses, she has made them an essential part of her travelogue by studying their structures and capturing them visually through her abstract paintings.
                        </p>
