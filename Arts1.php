                    <article><p>
                      Mr. Laxman Shrestha is an Accountant turned artist. He followed what his heart wanted, and to do so, he switched careers. This however was not taken very well by his family as they opposed his decision. He had to leave his family behind to pursue this dream of his, regardless of it being a minefield of hardship. It’s not that he’s up there, he still has issues that are hampering his progress. But he likes to take things in a more positive manner. This outlook of his fuels him to tirelessly work towards his goals. He has five more members in his group that help him with his dreams as they share it too. Raju Ghlan Tamang, Puskar Chamling Rai, Yambang Gahahood, and Bidesh Chamling Rai, all study BFA (Bachelors in Fine Arts) at the Lalit Kala Campus of Fine Arts, where Mr. Shrestha was a guest lecture. Not to forget the writer Shiva Kumar Sharma who has been a part of the team in their work. After being inspired by Shrestha’s words and his perspective on life, the 5 youngster joined him in his quest. And we must say that we too are inspired by him.
                        </p>
                    <p>
                        He had an interest in art since he was a child, but he never thought of it as a career.
                        His Interest reignited when one of his friend showed him a picture posted in a national daily. After seeing that photo, he really wanted to draw it. This was in 2010 between the periods of June – July. And then his love for art was put on hold when he pursued the career of an accountant.
                        </p>
                          <h2>HOW AND WHEN DID YOU DECIDE TO SWITCH CAREERS?</h2>
                      <p>
                        It took me 18 months to decide what I wanted to do with my life. Finally, in 2012, March 5, I decided that I would do what I love and just give up everything else.
                        While I was in Nepalgunj during those 18 months, I spent time making some street art on the side to explore and satisfy my artistic side.
                        During my time there, I wanted my art to appeal to the public. and in that process, I came up with the title “Sansad vs. Janta” (Members of the Parliament vs. Public) for my first art work. At that time I didn’t know about techniques or the basics, it was just one of my expressions put out in front of the public. This gave me a bit of popularity in Nepalgunj. It was then that I realized this is something I can see myself doing for the rest of my life.
                        </p>
                        <h3>IS THERE ANYONE WHO YOU IDOLIZE OR TAKE INSPIRATION FROM?</h3>
                         <br>

                        <p>
                        Mr. Hem Bahadhur Tamang has helped me a lot through my early days, he guided me and supported me always.
                        One day I was with him at Basantapur, and as you might know, there is a place where they distribute free food.
                        I told him that I was hungry and I’m going to line up there.
                        He said no, and gave me a thousand rupees note instead, and told me to spend it wisely.
                        </p>
                       <h4>YOU HAVE BEEN THROUGH A LOT. WHAT HAVE YOU LEARNED FROM THIS EXPERIENCE?</h4>
                      <p>
                        This one time, I fell extremely sick. A cycle being my only mode of transportation, I somehow managed to ride to the hospital, where I ended up screaming in pain.
                        I only had the remaining five hundred rupees with me and that was not enough for my treatment.
                        Even though my family members and other relatives lived close by to where I lived, no one stepped up to help me get through this rough and difficult time. I did manage to get the treatment done by scraping every penny I had, but this taught me a new thing: you should never depend upon anyone else.
                        </p>
