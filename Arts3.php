                    <article><p>
                      What would a picture of love look like? “O my Luve is like a red, red rose,”
                      wrote the 18th century Scottish poet Robert Burns. Three centuries now,
                      in artist Ragini Upadhyay Grela’s ongoing exhibition Love Revisited—wherein 17 out of the total 35 pieces have ‘Love’
                      in their titles—there are only lotuses and hearts, no roses.
                        </p>
                    <p>
                      Love Revisited is an amalgam of works, some created more than a decade ago—in 2007, 08 and 09—and more recent ones from 2015 and onwards.
                      The difference between the two lots is apparent. <br>
                      A majority of the earlier works feature nude, half-formed human figures holding appliances such as
                      monitors, mice, keyboards, receivers and cell phones, flying in the air.
                      In some pieces, the human figures are tightly clutching them while in others, they are  trying to get a hold of them.<br>
                      These works feel prophetic. The advent of social media has changed the way romantic partners meet. While previously, there was no way for partners to share their feelings other than by meeting in person, now they have telephones, cellphones and social media. Naturally, this revolutionary development has both pros and cons.
                      And Upadhyay Grela addresses them. <br></P>
                      <p>
                      Take, for instance, a piece called ‘Cross Culture’ which at first glance can easily be mistaken for a page from Kamasutra.
                      The focus of the piece is a copulating couple, slightly removed from the centre, sitting on a lotus flower.
                       One of them has multiple heads and hands, spread out in all directions.
                       None of their hands is free; they are holding a monitor, a mouse, a receiver, a cellphone and a keyboard.
                       Curiously, one of the hands is also holding a lotus flower.
                       Surely, the piece represents the havoc computers and other communication devices have wreaked in our lives
                       and our sense of spirit, but the one hand, curiously holding a lotus, makes one wonder how inner peace eludes us in this hyper-connected age. <br>
                       These pieces were created before 2010. In the more recent ones, however, modern computing appliances are surprisingly missing. ‘The Heart’ series features hearts, as implied. These pieces are more crisp and elegant than the earlier, more colourful, ones. Here, the background is all white. In the foreground lie hearts of different shapes, sizes and colours. They are bound by a single red thread. There are peacock feathers, denoting the cycle of time, and clocks. One particular piece features two hearts, the beaks of birds creep out of the pulmonary arteries, looking directly at each other. One of the beaks is holding a flower but the distance between them is wide and the red thread between them looks broken.
                       While the exhibit features 35 pieces, they seem too many for the cramped space of the Kathmandu Art Gallery at Le Sherpa. Some of the pieces are located so high on the wall, one has to stretch their head to get a view. And the glass-fenced wall of the room does a disservice to the exhibition, reflecting objects on the opposite side of the pieces. Placing titles on the sides of the paintings would have done no wrong as well.

                        </p>



